    public void initCarga()
    {
        //OBTENEMOS LA INFORMACIÃ“N DE LA CLAVE ACTUAL
        Q_empleado query = new Q_empleado();
        List<Empleado> lista= query.get_especifico(this.claveactual);
        Object[] fila = new Object[5];
        
        for(int i=0; i < lista.size(); i++)
        {
            String clave= Integer.toString(lista.get(i).getClave());
            this.i_clave.setText(clave);
            this.i_nombre.setText(lista.get(i).getNombr());
            
            
            
            double sue= lista.get(i).getSueld();
            int su = (int)sue;
            this.i_salario.getModel().setSelectedItem(su);
            
            double dia= lista.get(i).getDiast();
            int di = (int)dia;
            this.i_dias.getModel().setSelectedItem(di);
        }
        int sueldo = Integer.parseInt(this.i_salario.getSelectedItem().toString());
        int diastr = Integer.parseInt(this.i_dias.getSelectedItem().toString());
        
        int total = (sueldo*diastr);
        this.total.setText("$"+total+".00");
    }

