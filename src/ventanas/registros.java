    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        boolean b= jTable1.getSelectionModel().isSelectionEmpty();
        if(b == false)
        {
            int a = jTable1.getSelectedRow();//SELECCIONA EL NUMERO DE FILA DE LA TABLA
            TableModel model= jTable1.getModel();
            int val= Integer.parseInt(model.getValueAt(a,0).toString()); //OBTIENE EL VALOR DEL ATRIBUTO

            Q_empleado query = new Q_empleado();
            int res= query.borrar(val);
            if(res==1)
            {
                JOptionPane.showMessageDialog(null, "El registro se elimino correctamente",
                        "Excelente", JOptionPane.INFORMATION_MESSAGE);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "No fue posible eliminar el registro."+
                        "\nFavor de reintentar o actualizar la tabla",
                        "Algo va mal", JOptionPane.WARNING_MESSAGE);
            }
            initTabla();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "No es posible eliminar."+
                    "\nSelecciona un registro de la tabla y vuelve a intentar",
                    "Algo va mal", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

