    public void initTabla()
    {
        Q_empleado query = new Q_empleado();
        
        List<Empleado> lista = query.get_todos();
        
        DefaultTableModel modelotabla = (DefaultTableModel)jTable1.getModel();
        modelotabla.setRowCount(0);
        Object[] fila = new Object[5];
        
        for(int i=0; i < lista.size(); i++)
        {
            fila[0] = lista.get(i).getClave();
            fila[1] = lista.get(i).getNombr();
            fila[2] = lista.get(i).getSueld();
            fila[3] = lista.get(i).getDiast();
            fila[4] = lista.get(i).getTotal();
            modelotabla.addRow(fila);
        }
        jTable1.requestFocus();
    }

